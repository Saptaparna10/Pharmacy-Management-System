package com;

public class VendorDetails {
	private String vid;
	private String vpwd;
	private String vnm;
	private String vadd;
	private int vphn;
	private String vmail;
	private String desc;
	
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getVid() {
		return vid;
	}
	public void setVid(String vid) {
		this.vid = vid;
	}
	public String getVpwd() {
		return vpwd;
	}
	public void setVpwd(String vpwd) {
		this.vpwd = vpwd;
	}
	public String getVnm() {
		return vnm;
	}
	public void setVnm(String vnm) {
		this.vnm = vnm;
	}
	public String getVadd() {
		return vadd;
	}
	public void setVadd(String vadd) {
		this.vadd = vadd;
	}
	public int getVphn() {
		return vphn;
	}
	public void setVphn(int vphn) {
		this.vphn = vphn;
	}
	public String getVmail() {
		return vmail;
	}
	public void setVmail(String vmail) {
		this.vmail = vmail;
	}
	
	
}
